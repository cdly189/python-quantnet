"""
Some code to illustrate the use of abs() function
"""


def main():
    print('Enter two values and we will calculate the absolute value')
    val1 = int(input('Enter the first value: '))
    val2 = int(input('Enter the second value: '))

    print('The absolute value is: ')
    print(abs(val1-val2))


if __name__ == '__main__':
    main()
